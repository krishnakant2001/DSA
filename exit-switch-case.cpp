#include <bits/stdc++.h>
using namespace std;
int main()
{
    //exit(0) it is used to terminate the program
    
    int n;
    cin >> n;

    while(1){
        switch(n){
            case 1: cout << "1" << endl;
                exit(0);

            case 2: cout << "2" << endl;
                exit(0);

            default: cout << "0" << endl;
                exit(0);

            
        }
    }
    return 0;
}